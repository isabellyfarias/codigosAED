#include <iostream>
#include <string>
using namespace std;

class Agenda {
private:
    string nome;
    string telefone;

public:
    Agenda(string _nome, string _telefone) {
        nome = _nome;
        telefone = _telefone;
    }

    void CONTATO() {
        cout << "Nome: " << nome << endl;
        cout << "Telefone: " << telefone << endl;
    }
};

int main() {
   
    Agenda contato1("sergio", "6151996589");
    Agenda contato2("kamila", "6121443986");

   
    cout << "Contato 1:";
    contato1.CONTATO();

    cout << "\nContato 2:";
    contato2.CONTATO();

    return 0;
}

